def inject_contextual_anomalies(df, frac=INJECTION_FRACTION, seed=42):
    np.random.seed(seed)
    df = df.copy()
    df['is_anomaly']  = 0
    df['anomaly_type'] = 'normal'

    for (crop, region), data in df.groupby(['crop_type', 'region']):
        if len(data) < 15:
            continue
        n   = max(1, int(len(data) * frac))
        idx = np.random.choice(data.index, n, replace=False)
        seg = {f: {'low': data[f].quantile(0.10), 'high': data[f].quantile(0.90)}
               for f in FEATURES}

        for i in idx:
            atype = np.random.choice(
                ['drought_stress', 'heat_stress', 'sensor_inconsistency', 'crop_failure'],
                p=[0.25, 0.25, 0.25, 0.25])

            if atype == 'drought_stress':
                df.loc[i, 'soil_moisture_%'] = seg['soil_moisture_%']['low'] * 0.20
                df.loc[i, 'rainfall_mm']      = seg['rainfall_mm']['low']    * 0.30
                df.loc[i, 'NDVI_index']       = seg['NDVI_index']['low']     * 0.40
            elif atype == 'heat_stress':
                df.loc[i, 'temperature_C'] = seg['temperature_C']['high'] * 1.50
                df.loc[i, 'humidity_%']     = seg['humidity_%']['high']    * 1.30
            elif atype == 'sensor_inconsistency':
                df.loc[i, 'rainfall_mm']     = seg['rainfall_mm']['high']
                df.loc[i, 'soil_moisture_%'] = seg['soil_moisture_%']['low'] * 0.20
            elif atype == 'crop_failure':
                df.loc[i, 'NDVI_index']      = seg['NDVI_index']['low']     * 0.30
                df.loc[i, 'soil_moisture_%'] = seg['soil_moisture_%']['low'] * 0.20
                df.loc[i, 'temperature_C']   = seg['temperature_C']['high'] * 1.50

            df.loc[i, 'is_anomaly']   = 1
            df.loc[i, 'anomaly_type'] = atype

    n_anom = df['is_anomaly'].sum()
    print(f"Injected {n_anom} anomalies ({100*n_anom/len(df):.1f}%)")
    print(df['anomaly_type'].value_counts())
    return df

