def compute_final_score(row):
    key = (row['crop_type'], row['region'])
    if key not in if_models:
        return None
    X    = np.array([row[FEATURES]])
    if_s = float(norm_if.transform([[get_if_score(if_models[key], X)]])[0][0])
    lf_s = float(norm_lof.transform([[get_lof_score(lof_models[key], X)]])[0][0])
    ae_s = float(norm_ae.transform([[get_ae_score(autoencoders[key], scalers[key], X)]])[0][0])
    fs, _ = get_fusion_score(row, key)
    fu_s = float(norm_fus.transform([[fs]])[0][0])
    return (weights['if']     * if_s +
            weights['lof']    * lf_s +
            weights['ae']     * ae_s +
            weights['fusion'] * fu_s)

