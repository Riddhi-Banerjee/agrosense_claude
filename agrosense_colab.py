# 20. PREDICT FUNCTION — full report with type classification
# ============================================================
def predict_user_input(user_data):
    # Encode
    crop_enc   = (encoders['crop_type'].transform([user_data['crop_type']])[0]
                  if user_data['crop_type'] in encoders['crop_type'].classes_ else None)
    region_enc = (encoders['region'].transform([user_data['region']])[0]
                  if user_data['region'] in encoders['region'].classes_ else None)

    if crop_enc is None or region_enc is None:
        print("Invalid crop or region.")
        return
    key = (crop_enc, region_enc)
    if key not in if_models:
        print("No model for this crop-region combination.")
        return

    X = np.array([[user_data[f] for f in FEATURES]])

    if_r   = get_if_score(if_models[key], X)
    lof_r  = get_lof_score(lof_models[key], X)
    ae_r   = get_ae_score(autoencoders[key], scalers[key], X)
    fs_r, fusion_detail = get_fusion_score(user_data, key)

    if_s  = float(norm_if.transform([[if_r]])[0][0])
    lof_s = float(norm_lof.transform([[lof_r]])[0][0])
    ae_s  = float(norm_ae.transform([[ae_r]])[0][0])
    fus_s = float(norm_fus.transform([[fs_r]])[0][0])

    final_score = (weights['if']     * if_s  +
                   weights['lof']    * lof_s +
                   weights['ae']     * ae_s  +
                   weights['fusion'] * fus_s)

    print("\n" + "=" * 55)
    print(f"  ANOMALY DETECTION REPORT")
    print(f"  Crop: {user_data['crop_type']}  |  Region: {user_data['region']}")
    print("=" * 55)

    print(f"\nModel Scores (normalised 0-1):")
    print(f"  IF:     {if_s:.3f}  (weight: {weights['if']:.3f})")
    print(f"  LOF:    {lof_s:.3f}  (weight: {weights['lof']:.3f})")
    print(f"  AE:     {ae_s:.3f}  (weight: {weights['ae']:.3f})")
    print(f"  Fusion: {fus_s:.3f}  (weight: {weights['fusion']:.3f})")
    print(f"\nFinal Ensemble Score: {final_score:.3f}  |  Threshold: {best_thresh:.3f}")

    tendency_low = best_thresh - 0.05
    if final_score > best_thresh:
        print("\n  ⚠  ANOMALY DETECTED")
    elif final_score >= tendency_low:
        print("\n  🔮  TENDENCY TOWARDS ANOMALY — monitor closely")
    elif final_score >= 0.5:
        print("\n  ⚡  BORDERLINE CONDITIONS")
    else:
        print("\n  ✓  CONDITIONS NORMAL")

    # Parameter range issues
    if key in segment_stats:
        seg_s = segment_stats[key]
        issues = []
        for f in FEATURES:
            if f not in seg_s: continue
            val = user_data[f]
            low, high = seg_s[f]['low'], seg_s[f]['high']
            if   val < low:  issues.append(f"  {f}: {val:.2f} too LOW  (normal > {low:.2f})")
            elif val > high: issues.append(f"  {f}: {val:.2f} too HIGH (normal < {high:.2f})")
        if issues:
            print("\nParameter Issues:")
            for i in issues: print(i)

    # SHAP explanation
    exp = shap.TreeExplainer(if_models[key])
    sv  = exp.shap_values(X)[0]
    print("\nSHAP Feature Contributions (Isolation Forest):")
    for feat, val in sorted(zip(FEATURES, sv), key=lambda x: abs(x[1]), reverse=True):
        direction = "→ ANOMALY" if val > 0 else "→ NORMAL"
        print(f"  {feat:22s}: {val:+.4f}  {direction}")

    # AE feature reconstruction
    ae_feat  = get_ae_feature_scores(autoencoders[key], scalers[key], X)
    top_ae   = sorted(ae_feat.items(), key=lambda x: x[1], reverse=True)
    print("\nAutoencoder — Top Anomalous Features:")
    for feat, err in top_ae[:3]:
        print(f"  {feat:22s}: reconstruction error = {err:.5f}")

    # Sensor fusion z-scores
    if fusion_detail:
        print("\nSensor Fusion — Learned Relationship Deviations:")
        for pair, z in fusion_detail.items():
            flag = " ← SUSPICIOUS" if z > 2.0 else ""
            print(f"  {pair:35s}: z-score = {z:.2f}{flag}")

    # [CLS] Anomaly type diagnosis
    if final_score > best_thresh:
        cls_input = np.array([[if_s, lof_s, ae_s, fus_s] +
                               [user_data[f] for f in FEATURES]])
        cls_pred  = type_classifier.predict(cls_input)[0]
        cls_proba = type_classifier.predict_proba(cls_input)[0]
        cls_classes = type_classifier.classes_
        print(f"\n[DIAGNOSIS] Probable anomaly type: {cls_pred.upper().replace('_',' ')}")
        print("  Probability breakdown:")
        for cls, prob in sorted(zip(cls_classes, cls_proba),
                                key=lambda x: x[1], reverse=True):
            bar = '█' * int(prob * 20)
            print(f"    {cls:25s}: {prob:.3f}  {bar}")

    # Geospatial context
    lat = user_data.get('latitude', None)
    lon = user_data.get('longitude', None)
    if lat is not None and lon is not None:
        lat_n = (lat - df_eval['latitude'].min()) / (df_eval['latitude'].max() - df_eval['latitude'].min() + 1e-9)
        lon_n = (lon - df_eval['longitude'].min()) / (df_eval['longitude'].max() - df_eval['longitude'].min() + 1e-9)
        # Assign to nearest cluster centroid
        nearby = df_eval[
            (abs(df_eval['latitude'] - lat) < 2.0) &
            (abs(df_eval['longitude'] - lon) < 2.0)
        ]
        if len(nearby) > 0:
            local_anom_rate = nearby['is_anomaly'].mean()
            print(f"\n[GEO] Nearby farm anomaly rate: {local_anom_rate*100:.1f}% "
                  f"({len(nearby)} farms within 2° radius)")

    # Yield prediction
    pred_yield = formula_yield(user_data, crop_enc)
    crop_name  = user_data['crop_type']
    crop_avg   = _base_yields.get(crop_name, 4033)
    diff_pct   = (pred_yield - crop_avg) / crop_avg * 100
    print(f"\nPredicted Yield: {pred_yield:.0f} kg/ha  "
          f"({diff_pct:+.1f}% vs {crop_name} average of {crop_avg:,} kg/ha)")
    print("=" * 55 + "\n")

